    <div class="post-text"><p>Si partimos los años que tengo a la mitad, nos encontraremos en un verano en el estado de México, viviendo durante dos meses en la honorable ESC PRIM RUR FED Beníto Juárez, del humilde pueblo de San Juan Atzingo.</p>
        <p>&nbsp;</p>
        <p>Y allá yo, en una inolvidable campaña de afabetización que cada año realizaba mi colegio. Verdor infinito de campo, colinas, las mejores tortillas de maíz del mundo, hechas a mano por supuesto.</p>
        <p>&nbsp;</p>
        <p>Aprendí y enseñé. Aprendí cómo se hace el pulque, y enseñé a Juanita, de setenta y tantos, a contar y a sumar con los olotes tirados en el piso de tierra. Aprendí que las personas pueden trabajar 12 horas al día cultivando chícharos, por 18 pesos, y enseñé que San Juan está en el estado de México, en la República Mexicana, en América, en el Mundo, en el Sistema Solar (que dicho sea de paso, aún tenía a Plutón). Aprendí que nunca te debes de quedar dormida en un cuarto que ha sido intensamente fumigado con Baigón verde, riesgo de una severa intoxicación, y enseñé qué era una vocal, una sílaba y una palabra. Y así podría seguir, recordando el olor de la tierra arcillosa, y las luciérnagas y, de fondo, durante toda la campaña, el CD llamado Silvio, que le regalamos a Paloma en su cumpleaños.</p>
        <p>&nbsp;</p>
        <p>Una época llena de esperanzas en el futuro, de utopías, de intentar cambiar al mundo llevando las armas del conocimiento a los que más lo necesitaban. Dos meses amargos y hermosos: de amistad, de trabajo duro, de baños de agua helada, de cocinar para una tropa cuando no sabes ni freir un huevo, de subir hasta la última casa del cerro, bajo la lluvia para dar clase, de empezar a conocer por mi misma el mundo hostil, completamante desconocido hasta entonces.</p>
        <p>&nbsp;</p>
        <p>Écho de menos esas épocas llenas de sueños y de compañeros inteligentes y entusiastas. Hace añísimos que no los veo, pero seguramente les ha pasado lo que nos pasa a todos con la edad&#8230; un alud de rutinas y de desiluciones se precipita para tratar de enterrar el espíritu de lucha y cambio. O tal vez no, y en alguna parte de este país estén en este momento preparándose para salir a tomar las calles mañana y gritar a todo pulmón &#8220;NO A LA IMPOSICIÓN&#8221;</p>
